
public class Menu_Item {
    private
         int id;
         String name;
         String menu_Desc;
         String Description;
         double price;
         int calories;
         int onmenu;
         String cooktime;
         int spicy;
         int recommended;
     public
          void setId(int arg)
          {
              id=arg;
          }
          int getId()
          {
              return id;
          }
          
          void setName(String arg)
          {
              name=arg;
          }
          String getName()
          {
              return name;
          }
          
          void setMenuDesc(String arg)
          {
              menu_Desc=arg;
          }
          String getMenuDesc()
          {
              return menu_Desc;
          }
          
          void setDescription(String arg)
          {
              Description=arg;
          }
          String getDescription()
          {
              return Description;
          }
          
          void setPrice(double arg)
          {
              price=arg;
          }
          double getPrice()
          {
              return price;
          }
          
          void setCalories(int arg)
          {
              calories=arg;
          }
          int getCalories()
          {
              return calories;
          }
          
          void setOnMenu(int arg)
          {
              onmenu=arg;
          }
          int getOnMenu()
          {
              return onmenu;
          }
          
          void setCookTime(String arg)
          {
              cooktime=arg;
          }
          String getCookTime()
          {
              return cooktime;
          }
          
          void setSpicy(int arg)
          {
              spicy=arg;
          }
          int getSpicy()
          {
              return spicy;
          }
          
          void setRecommended(int arg)
          {
              recommended=arg;
          }
          int getRecommended()
          {
              return recommended;
          }
}
