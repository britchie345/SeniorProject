public class Type {
    private
    int typeID;
    String name;
    String description;
    public
    
    void setTypeId(int arg)
    {
        typeID=arg;
    }
    int getTypeId()
    {
        return typeID;
    }
    
    void setName(String arg)
    {
        name=arg;
    }
    String getName()
    {
        return description;
    }
    
    void setDescription(String arg)
    {
        description=arg;
    }
    String getDescription()
    {
        return description;
    }
    
}
